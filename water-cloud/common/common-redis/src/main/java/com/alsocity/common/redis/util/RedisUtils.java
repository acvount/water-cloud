    /**
     * 全局id队列名称
     */
    private static final String QUEUE_CREATE_ID = "QUEUE_CREATE_ID";

    /**
     * 相对类左侧插入元素
     * 头部入队
     *
     * @param queueName
     * @param id
     */
    public Long leftPush(String queueName, Long id) {
        if (StrUtil.isBlank(queueName)) {
            queueName = QUEUE_CREATE_ID;
        }
        return redisTemplate.opsForList().leftPush(queueName, id);
    }

    /**
     * 相对类左侧插入对象
     * 头部入队
     *
     * @param queueName
     * @param object
     */
    public Long leftPush(String queueName, Object object) {
        if (StrUtil.isBlank(queueName)) {
            queueName = QUEUE_CREATE_ID;
        }
        return redisTemplate.opsForList().leftPush(queueName, object);
    }

    /**
     * 从尾部弹出队列元素
     *
     * @param queueName
     * @return
     */
    public Object rightPull(String queueName) {
        if (StrUtil.isBlank(queueName)) {
            queueName = QUEUE_CREATE_ID;
        }
        //Object strJson = operation.rightPop(template_Queue, 10, TimeUnit.SECONDS);
        // 5, TimeUnit.SECONDS
        return (Object) redisTemplate.opsForList().rightPop(queueName, 500, TimeUnit.MILLISECONDS);
    }

    /**
     * 从尾部弹出队列元素
     *
     * @param queueName
     * @return
     */
    public Long rightPop(String queueName) {
        if (StrUtil.isBlank(queueName)) {
            queueName = QUEUE_CREATE_ID;
        }
        //Object strJson = operation.rightPop(template_Queue, 10, TimeUnit.SECONDS);
        // 5, TimeUnit.SECONDS
        return (Long) redisTemplate.opsForList().rightPop(queueName, 500, TimeUnit.MILLISECONDS);
    }

    //===================== pipeline=============

    /**
     * pipeline向list插入
     *
     * @param queueName 队列名
     * @param idList    待插入的值集合
     * @return
     */
    public Boolean lPushByPipeline(String queueName, List<Long> idList) {
        try {
            //批量set数据
            RedisSerializer<Serializable> keySerializer = (RedisSerializer<Serializable>) redisTemplate.getKeySerializer();

            byte[] rawKey = keySerializer.serialize(queueName);

            RedisSerializer<Serializable> valueSerializer = (RedisSerializer<Serializable>) redisTemplate.getValueSerializer();

            redisTemplate.executePipelined(new RedisCallback<String>() {
                @Override
                public String doInRedis(RedisConnection connection) throws DataAccessException {
                    for (Long id : idList) {
                        byte[] rawValue = valueSerializer.serialize(id);
                        connection.lPush(rawKey, new byte[][]{rawValue});
                    }
                    return null;
                }
            });
            return true;
        } catch (Exception e) {
            log.error("pipeline向list插入出错:{}", e);
            return false;
        }
    }
